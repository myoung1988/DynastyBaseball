	BEGIN
		SELECT 'After: ', * FROM #TmpSchedule
		ROLLBACK
	END ELSE
	BEGIN
		INSERT INTO Fantasy.Schedule
		SELECT @FantasyLeagueID,
				PeriodID,
				@SeasonID,
				StartDate,
				EndDate, 
				Playoffs
		FROM #TmpSchedule

		COMMIT
	END

END TRY
BEGIN CATCH
